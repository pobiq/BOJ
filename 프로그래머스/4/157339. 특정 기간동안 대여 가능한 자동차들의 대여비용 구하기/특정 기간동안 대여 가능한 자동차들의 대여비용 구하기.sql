SELECT C.CAR_ID, C.CAR_TYPE, ROUND(C.DAILY_FEE * 30 * (100 - P.DISCOUNT_RATE) / 100, 0) AS FEE
FROM CAR_RENTAL_COMPANY_CAR C JOIN CAR_RENTAL_COMPANY_DISCOUNT_PLAN P
ON P.CAR_TYPE = C.CAR_TYPE AND DURATION_TYPE = '30일 이상'
WHERE C. CAR_ID NOT IN(
    SELECT CAR_ID 
    FROM CAR_RENTAL_COMPANY_RENTAL_HISTORY
    WHERE START_DATE < '2022-12-01 00:00:00' AND END_DATE >= '2022-11-01 00:00:00'
)
AND C.CAR_TYPE IN('세단', 'SUV')
AND ROUND(C.DAILY_FEE * 30 * (100 - P.DISCOUNT_RATE) / 100, 0) >= 500000 
AND ROUND(C.DAILY_FEE * 30 * (100 - P.DISCOUNT_RATE) / 100, 0) < 2000000
